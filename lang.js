window.sfLang = localStorage.getItem('sf_lang') || 'bg';

window.changeLang = function (l) {
    localStorage.setItem('sf_lang', l);
    window.location.reload();
};

window.t = function (str) {
    if (!str || typeof str !== 'string') return str;
    if (window.sfLang === 'bg') return str;
    let clean = str.trim();
    let trans = window.DICTIONARY[window.sfLang]?.[clean];
    if (trans) return str.replace(clean, trans);

    // Also handle dynamic strings
    let out = str;
    if (window.sfLang === 'en') {
        out = out.replace('лв.', 'BGN').replace(' ч', ' h').replace('ч.', 'h.').replace('ч', 'h').replace('см.', 's.');
    } else if (window.sfLang === 'ru') {
        out = out.replace('лв.', 'BGN').replace(' ч', ' ч').replace('ч.', 'ч.').replace('ч', 'ч').replace('см.', 'см.');
    }
    return out;
}

function translateDOM(node) {
    if (window.sfLang === 'bg') return;

    if (node.nodeType === Node.TEXT_NODE) {
        let raw = node.textContent;
        let clean = raw.trim();
        if (clean && window.DICTIONARY[window.sfLang]?.[clean]) {
            node.textContent = raw.replace(clean, window.DICTIONARY[window.sfLang][clean]);
        } else if (clean === 'лв.') {
            node.textContent = raw.replace('лв.', window.sfLang === 'en' ? 'BGN' : 'BGN');
        }
    } else if (node.nodeType === Node.ELEMENT_NODE) {
        if (node.tagName === 'SCRIPT' || node.tagName === 'STYLE') return;

        if (node.placeholder) {
            let ph = node.placeholder.trim();
            if (window.DICTIONARY[window.sfLang]?.[ph]) {
                node.placeholder = window.DICTIONARY[window.sfLang][ph];
            }
        }
        if (node.title) {
            let ti = node.title.trim();
            if (window.DICTIONARY[window.sfLang]?.[ti]) {
                node.title = window.DICTIONARY[window.sfLang][ti];
            }
        }

        for (let c of node.childNodes) {
            translateDOM(c);
        }
    }
}

document.addEventListener("DOMContentLoaded", () => {
    document.title = window.t(document.title);
    translateDOM(document.body);

    // MutationObserver to instantly translate inserted nodes by app.js
    const observer = new MutationObserver(mutations => {
        mutations.forEach(m => {
            m.addedNodes.forEach(n => {
                if (n.nodeType === Node.ELEMENT_NODE) {
                    translateDOM(n);
                }
            });
            if (m.type === 'characterData') {
                let str = m.target.textContent.trim();
                let tr = window.DICTIONARY[window.sfLang]?.[str];
                if (tr) m.target.textContent = m.target.textContent.replace(str, tr);
            }
        });
    });
    observer.observe(document.body, { childList: true, subtree: true, characterData: true });

    // Set language selector value if it exists
    const selects = document.querySelectorAll('#lang-select');
    selects.forEach(sel => sel.value = window.sfLang);
});
